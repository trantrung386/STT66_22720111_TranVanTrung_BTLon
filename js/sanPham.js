let itemsPerPage = 12; 
let currentIndex = 0; 


function displayProducts(products, containerId) {
    const container = $(`#${containerId}`);
    let productHtml = '<div class="row">';
    
    for (let i = currentIndex; i < currentIndex + itemsPerPage && i < products.length; i++) {
        const product = products[i];
        productHtml += `
            <div class="col-md-3 col-sm-6 product-small col has-hover" id="pc-${product.id}">
                <div class="col-inner">
                    <div class="product-small box">
                        <div class="box-image">
                            <img class="default-image" width="100%" src="${product.image}" alt="${product.title}">
                        </div>
                        <div class="box-text text-center">
                            <p class="name">
                                <a href="#" class="product-link" data-product-id="${product.id}">${product.title}</a>
                            </p>
                            <div class="box-image">
                                <img class="default-image" width="100%" src="${product.tag}" alt="${product.title}">
                            </div>
                            <span class="price"><bdi>${product.price}&nbsp;<span>đ</span></bdi></span>
                            <button class="btn btn-primary add-to-cart" data-product-id="${product.id}">Thêm giỏ hàng</button>
                        </div>
                    </div>
                </div>
            </div>
        `;
        
    }
    $(document).on('click', '.product-link', function(event) {
        const productId = $(this).data('product-id');
        viewProductDetail(productId);
        window.location.href = "../html/chiTietSp.html"; // Chuyển hướng đến trang chi tiết sản phẩm
    });

    localStorage.setItem('listProduct', JSON.stringify(listProduct));
    
    productHtml += '</div>'; 
    container.append(productHtml); 
    
    
    
    
    currentIndex += itemsPerPage; // Cập nhật vị trí cho lần hiển thị tiếp theo
    
    // Ẩn nút "Xem thêm" khi đã hiển thị hết sản phẩm
    if (currentIndex >= products.length) {
        $('#loadMore').hide();
    }
}
function viewProductDetail(productId) {
    const product = listProduct.find(item => item.id === productId);
    if (product) {
        localStorage.setItem("selectedProduct", JSON.stringify(product));
    }
}

$(document).on('click', '.product-link', function(event) {
    const productId = $(this).data('product-id');
    viewProductDetail(productId);
});


// Gọi hàm để hiển thị sản phẩm lần đầu
displayProducts(listProduct, 'listProduct');

// Thêm sự kiện cho nút "Xem thêm"
$('#loadMore').click(function() {
    displayProducts(listProduct, 'listProduct');
});

